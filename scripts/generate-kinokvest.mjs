function buildQuestion(q, value) {
  return {
    id: uid(),
    value,
    question: q.question,
    answer: q.answer,
    questionMedia: [],
    answerMedia: []
  }
}

function buildCategory(cat, roundIndex, catIndex) {
  const questions = (cat.questions || []).slice(0, 6).map((q, i) =>
    buildQuestion(q, VALUES[i])
  )
  return {
    id: uid(),
    title: cat.title || `Категория ${catIndex + 1}`,
    questions
  }
}

function buildRound(round, roundIndex) {
  return {
    id: uid(),
    title: round.title || `Раунд ${roundIndex + 1}`,
    categories: round.categories.map((cat, i) => buildCategory(cat, roundIndex, i))
  }
}

